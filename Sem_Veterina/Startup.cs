using Microsoft.EntityFrameworkCore;
using Sem_Veterina.Controllers;

namespace Sem_Veterina
{
    public class Startup
    {
        public IConfiguration Configuration { get; }

        public Startup(IConfiguration configuration)
        {
            Configuration = configuration;
        }

        public void ConfigureServices(IServiceCollection services)
        {
            services.AddDbContext<OracleDbContext>(options =>
                options.UseOracle(Configuration.GetConnectionString("OracleDbContext")));
            // Registrovat DbContext


            // Registrovat DbTestService jako singleton, scoped nebo transient
            services.AddTransient<DbTestService>();

            // Přidat MVC služby nebo kontrolery


            services.AddControllersWithViews();
        }

        public void Configure(IApplicationBuilder app, IWebHostEnvironment env)
        {
            if (env.IsDevelopment())
            {
                app.UseDeveloperExceptionPage();
            }
            else
            {
                app.UseExceptionHandler("/Home/Error");
                app.UseHsts();
            }

            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.UseEndpoints(endpoints =>
            {
                endpoints.MapControllerRoute(
                    name: "default",
                    pattern: "{controller=Login}/{action=Index}/{id?}");
            });
        }
    }
}
